    public void guardarArchivo()
    {
        Archivo nuevo = new Archivo();
        nuevo.setName(nombreText.getText());//Se nombre al nuevo fichero que ser√° creado
        try {
            nuevo.crearArchivo();
            nuevo.setRegistroEstudiantes(registroEstudiantes);
            nuevo.guardarInformacion();//Metodo de la clase Archivo
            
        } catch (IOException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(null, "Archivo guardado");
        
        this.hide();
        form2.setVisible(true);
        form2.enable(true); 
    }
    
    public void salir()
    {
        this.hide();
        form2.setVisible(true);
        form2.enable(true);
    }
    
